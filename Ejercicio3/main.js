

let radio = parseInt(prompt("Ingrese el valor del radio"));

let altura = parseInt(prompt("Ingrese el valor de la altura"));

let area =  2 * Math.PI * radio * altura;

let volumen = Math.PI * radio * radio + altura;


alert(`El area del cilindro es:  ${area}`);

alert(`El volumen del cilindro es:  ${volumen}`);



